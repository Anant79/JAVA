    public void NetSalary(){
        String BASIC_SALARY=jTextField10.getText().trim();
        float B_S = Float.parseFloat(BASIC_SALARY) ;
        
        String GROSS_DEDUCTIONA=jTextField17.getText().trim();
        float G_D = Float.parseFloat(GROSS_DEDUCTIONA) ;
        
        float NET_SALARY = B_S - G_D ;
        jTextField18.setText(Float.toString(NET_SALARY));
    }
    
