    public void AllEmployee(){
        if (jTable1.getRowCount() > 0) 
        {
                while (model.getRowCount() > 0) 
                {
                    model.removeRow(0);
                }
        }
        try{
                con=DatabaseHelper.getConnection();
                st = con.createStatement();
                //rs = st.executeQuery("SELECT * FROM EMPLOYEE");
                rs = st.executeQuery("SELECT newemployee.empid,name,designation,department,advancepay,professionaltax,loan,provisionalfund FROM newemployee,deductioninfo WHERE newemployee.empid=deductioninfo.empid");
                while (rs.next()) 
                {              
                    model.insertRow(jTable1.getRowCount(), new Object[]{rs.getString(1), rs.getString(2),rs.getString(3),rs.getString(4),rs.getString(5),rs.getString(6),rs.getString(7),rs.getString(8)});
                }
            }
            catch(Exception e){
                System.out.println(e);
            }
    }
    
        
